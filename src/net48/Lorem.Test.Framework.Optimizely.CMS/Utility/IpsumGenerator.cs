        public static string GenerateMobilePhoneNumber()
        {
            Random random = new Random(Seed++ + DateTime.Now.Second);

            return string.Format("{0}{1}{2}-{3} {4} {5}", random.Next(0, 9), random.Next(0, 9), random.Next(0, 9),
                random.Next(10, 99), random.Next(10, 99), random.Next(100, 999));
        }

        public static string GenerateEmail(bool forceUnique = false)
        {
            if (forceUnique)
            {
                return $"{Guid.NewGuid().ToString("N")}@autogen.lorem";
            }

            string email = string.Format("{0}.{1}@autogen.lorem", Generate(1, false), Generate(1, false));
            return email.ToLower();
        }

        public static string GenerateName()
        {
            Random random = new Random(Seed++ + DateTime.Now.Second);
            int randomNameIndex = random.Next(names.Length - 1);
            string firstname = names[randomNameIndex];

            int randomLastnameIndex = random.Next(lastnames.Length - 1);
            string lastname = lastnames[randomLastnameIndex];

            return $"{firstname} {lastname}";
        }

        public static string Generate(int minWords, int maxWords, bool includeDot = true)
        {
            Random random = new Random(Seed++ + DateTime.Now.Second);
            return Generate(random.Next(minWords, maxWords), includeDot);
        }

        public static string Generate(int totalWords, bool includeDot = true)
        {
            StringBuilder builder = new StringBuilder();

            Random random = new Random(Seed++ + DateTime.Now.Second);
            double dotAfterWord = (int)(totalWords * random.NextDouble());
            bool nextCharUpperCase = true;

            for (int index = 0; index < totalWords; index++)
            {
                if (includeDot && index != 0 && index % dotAfterWord == 0)
                {
                    builder.Append(". ");
                    nextCharUpperCase = true;
                    dotAfterWord = (int)(totalWords * random.NextDouble());
                }

                int randomWordIndex = random.Next(words.Length - 1);
                string randomWord = words[randomWordIndex];

                if (nextCharUpperCase)
                {
                    randomWord = char.ToUpper(randomWord[0]) + randomWord.Substring(1);
                    nextCharUpperCase = false;
                }

                builder.Append(randomWord).Append(' ');
            }

            if (includeDot)
            {
                builder.Append(".");
            }

            string value = builder.ToString().Trim();
            return value.Replace(" .", ".");
        }

        public static int Random(int min, int max)
        {
            Random random = new Random(Seed++ + DateTime.Now.Second);
            return random.Next(min, max);
        }

